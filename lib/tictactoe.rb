require_relative './tictactoe/board.rb'
require_relative './tictactoe/human_player.rb'
require_relative './tictactoe/computer_player.rb'

class Game
  attr_accessor :board, :player_one, :player_two

  def self.start_game
    new.start_game
  end

  def initialize
    @board = Tictactoe::Board.new
    @player_one = Tictactoe::HumanPlayer.new(Tictactoe::Board::MARKERS[1], board)
    @player_two = Tictactoe::ComputerPlayer.new(Tictactoe::Board::MARKERS[0], board)
  end

  def start_game
    print_game_start
    run_game while !game_finished?
    puts 'Game over'
  end

  private

  def print_game_start
    board.print_board
    puts 'Enter [0-8]:'
  end

  def run_game
    player_one.play_turn
    player_two.play_turn if !game_finished?
    board.print_board
  end

  def game_finished?
    board.game_is_over_with_winner? || board.tie?
  end
end
